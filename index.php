             <h3 style="padding-top:2em;">Our Story</h3><hr>
             <p class="story">Again this year 20% of the Illinois Relief Sale net income will be used locally by grassroots volunteers to provide food for the needy in Central Illinois.</p>
             
             <p class="story">Also, as in the past, 80% of the net proceeds from the Sale are channeled through Mennonite Central Committee (MCC), an international relief and service agency located in Akron, Pennsylvania. Mennonite Central Committee, a worldwide ministry of Anabaptist churches, shares God’s love and compassion for all by responding to basic human needs and by working for peace and justice. MCC began in 1920 in Russian and Ukrainian villages needing assistance. Today MCC celebrates 94 years (2012-2014) of serving the needy in the name of Christ.</p>
             
             <p class="story">Presently, MCC has over 1,000 workers volunteering in 60 countries of the world. The 2014 MCC calendar theme is “Sharing God's Love and Compassion.” MCC is a hands-on, concrete ministry that addresses basic human needs such as water, food, and shelter; it works with churches and communities in a variety of efforts to serve the less fortunate and to build peace.</p>
